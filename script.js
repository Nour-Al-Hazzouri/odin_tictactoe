// Gameboard object for gameboard-related functionality
const Gameboard = (function () {

    // Board variables
    const gameBoard = [];
    const boardLength = 9;
    let markersPlaced = 0;

    // Player related variables
    markerX = 'X';
    markerO = 'O';
    let playerOne, playerTwo, playerOnesName, playerTwosName;
    const rows = document.querySelectorAll('[data-id]');
    function createPlayers() {
        playerOne = Player(playerOnesName, markerX);
        playerTwo = Player(playerTwosName, markerO);
        Game.initiateGame(rows);
    }
    return {
        // Reset all values in array from undefined to 0
        extractNames: () => {
            const nameOne = document.querySelector("#p1").value;
            const nameTwo = document.querySelector("#p2").value;
            if (!(nameOne && nameTwo)) {
                alert("Name/s cannot be empty!");
            } else {
                playerOnesName = nameOne;
                playerTwosName = nameTwo;
                createPlayers();
                startButton.textContent = "Restart";
            }
        },
        defineNames: (playerOne, playerTwo) => {
            const header = document.querySelector('header');
            const h2 = document.createElement('h2');
            h2.textContent = `${playerOne} is X, ${playerTwo} is O!`;
            header.appendChild(h2);
        },
        resetBoard: () => {
            for (let i = 0; i < boardLength; i++) {
                if (!(gameBoard[i] === 0)) {
                    gameBoard[i] = 0;
                }
                markersPlaced = 0;
                rows.forEach(row => {
                    row.textContent = '';
                    row.disabled = false;
                })
            }
        },
        // Logic to insert marker in array, array termination, and array iteration with edge cases
        insertMarker: (e) => {
            const currentButton = e.target;
            const currentButtonIndex = e.target.dataset.id;

            const playerOneMarker = playerOne.getMarker();
            const playerTwoMarker = playerTwo.getMarker();

            if (gameBoard[currentButtonIndex] === 0 && currentButtonIndex < boardLength && currentButtonIndex >= 0) {
                if (markersPlaced % 2 === 0 && !(currentButton.textContent)) {
                    gameBoard[currentButtonIndex] = playerOneMarker;
                    currentButton.textContent = playerOneMarker;
                } else {
                    gameBoard[currentButtonIndex] = playerTwoMarker;
                    currentButton.textContent = playerTwoMarker;
                }
                markersPlaced += 1;
            }
            Gameboard.checkResults()
        },
        checkResults: () => {
            if ( // Logic by me, conditions generated by AI to fasten code typing.
                (gameBoard[0] !== 0 &&
                    gameBoard[1] !== 0 &&
                    gameBoard[2] !== 0 &&
                    gameBoard[0] === gameBoard[1] &&
                    gameBoard[1] === gameBoard[2]) ||
                (gameBoard[3] !== 0 &&
                    gameBoard[4] !== 0 &&
                    gameBoard[5] !== 0 &&
                    gameBoard[3] === gameBoard[4] &&
                    gameBoard[4] === gameBoard[5]) ||
                (gameBoard[6] !== 0 &&
                    gameBoard[7] !== 0 &&
                    gameBoard[8] !== 0 &&
                    gameBoard[6] === gameBoard[7] &&
                    gameBoard[7] === gameBoard[8]) ||
                (gameBoard[0] !== 0 &&
                    gameBoard[3] !== 0 &&
                    gameBoard[6] !== 0 &&
                    gameBoard[0] === gameBoard[3] &&
                    gameBoard[3] === gameBoard[6]) ||
                (gameBoard[1] !== 0 &&
                    gameBoard[4] !== 0 &&
                    gameBoard[7] !== 0 &&
                    gameBoard[1] === gameBoard[4] &&
                    gameBoard[4] === gameBoard[7]) ||
                (gameBoard[2] !== 0 &&
                    gameBoard[5] !== 0 &&
                    gameBoard[8] !== 0 &&
                    gameBoard[2] === gameBoard[5] &&
                    gameBoard[5] === gameBoard[8]) ||
                (gameBoard[0] !== 0 &&
                    gameBoard[4] !== 0 &&
                    gameBoard[8] !== 0 &&
                    gameBoard[0] === gameBoard[4] &&
                    gameBoard[4] === gameBoard[8]) ||
                (gameBoard[2] !== 0 &&
                    gameBoard[4] !== 0 &&
                    gameBoard[6] !== 0 &&
                    gameBoard[2] === gameBoard[4] &&
                    gameBoard[4] === gameBoard[6])
            ) {
                Game.announceWinner(playerOnesName, playerTwosName);
                rows.forEach(row => {
                    row.disabled = true;
                })
            }
        },
        lastPlayed: () => {
            let lastMarker;
            if (markersPlaced % 2 === 0) {
                lastMarker = 2;
            } else {
                lastMarker = 1;
            }
            return lastMarker;
        },
        tieDetector: () => {
            let tieChecker = 0;
            for (let i = 0; i < boardLength; i++) {
                if ((Gameboard.logBoardIndex(i) === 'X') || (Gameboard.logBoardIndex(i) === 'O')) {
                    tieChecker += 1;
                }
            }
            if (tieChecker === 9)
                alert("Tie");
        },
        getWinnersName: () => {
            if (Gameboard.lastPlayed() === 1) {
                return playerOne.getName();
            } else {
                return playerTwo.getName();
            }
        }
    };
})();

function Player(playerName, playerMarker) {
    const getName = () => {
        return playerName;
    };
    const getMarker = () => {
        return playerMarker;
    };
    return { getName, getMarker };
}

const Game = (function () {
    return {
        initiateGame: (rows) => {
            Gameboard.resetBoard();
            Gameboard.extractNames()
            rows.forEach((row) => {
                row.addEventListener('click', Gameboard.insertMarker);
            });

        },
        announceWinner: (playerOnesName, playerTwosName) => {
            if (Gameboard.lastPlayed() === 1) {
                alert(`${playerOnesName} Won!`);
            } else if (Gameboard.lastPlayed() === 2) {
                alert(`${playerTwosName} Won!`)
            } else if (!(Gameboard.lastPlayed)) {
                Gameboard.tieDetector();
            }
        },
    };
})();

const startButton = document.querySelector('.start-btn');
startButton.addEventListener('click', Game.initiateGame(startButton));
